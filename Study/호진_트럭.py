# 백준 13335 트럭 실버1
import sys
input = sys.stdin.readline

# 입력값 읽기: 트럭의 수 n, 다리의 길이 w, 최대하중 L
n, w, L = map(int, input().split())

# 입력값 읽기: 트럭들의 무게
trucks = list(map(int, input().split()))

# 다리를 나타내는 큐(길이가 w인 리스트, 초기값은 모두 0)
# 큐는 다리 위에 있는 트럭의 위치를 시뮬레이션함
q = [0] * w

# 다리의 현재 위치를 가리키는 인덱스 (큐의 맨 앞)
idx = 0

# 다리 위의 트럭 무게의 합
ss = 0

# 전체 소요 시간
sec = 0

# 트럭을 처리하는 인덱스
i = 0

# 모든 트럭이 다리를 건널 때까지 반복
while i < n:
    sec += 1  # 시간 1초 경과
    t = trucks[i]  # 현재 처리 중인 트럭의 무게
    
    # 다리의 큐에서 트럭이 나가는 경우
    if q[idx] != 0:  # 큐의 현재 위치에 트럭이 있는 경우
        ss -= q[idx]  # 다리 위의 총 무게에서 제거
        q[idx] = 0    # 트럭이 다리에서 나갔으므로 0으로 초기화
    
    # 다리 위에 트럭을 올릴 수 있는 경우
    if ss + t <= L:  # 현재 트럭의 무게를 추가해도 최대 하중을 초과하지 않는다면
        ss += t       # 다리 위의 총 무게를 갱신
        q[idx] = t    # 현재 위치에 트럭 추가
        i += 1        # 다음 트럭 처리
    
    # 다리의 현재 위치를 다음으로 이동 (원형 큐처럼 동작)
    idx = (idx + 1) % w

# 모든 트럭이 다리를 빠져나오도록 다리의 길이(w)만큼 추가 시간 필요
print(sec + w)
